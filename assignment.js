// Declare variables with different data types
let myString = "Hello, world!";
let myBoolean = true;
let myUndefined = undefined;
let myNull = null;

// Using typeof operator to check data types
console.log(typeof myString); // Output: string
console.log(typeof myBoolean); // Output: boolean
console.log(typeof myUndefined); // Output: undefined
console.log(typeof myNull); // Output: object (Note: typeof null is 'object', which is a JavaScript quirk)

// Arithmetic operations
console.log(1 + 1); // Output: 2
console.log(3 - 2); // Output: 1
console.log(5 / 4); // Output: 1.25
console.log(4 * 2); // Output: 8
console.log(5 % 2); // Output: 1 (remainder of 5 divided by 2)
console.log(3 ** 2); // Output: 9 (3 squared)

